const getProducts = (count, list) => {
    const max = list.length - count
    const min = 0
    const start = Math.floor(Math.random() * (max - min) + min)
    return list.slice(start, start + count)
}
export const getListProduct = (count) => {
    const lb = listProduct.map(item => {

        return {...item, products: getProducts(count, item.products)}
    } 
    )
    return lb;
}
export const getAllProducts = () => {
    let product = []
    listProduct.forEach(item => {
        product = product.concat(item.products)
    });

    return product;
}
export const getAllProductByPage = (page) => {
    const pageSize = 10; 
    const start = pageSize*(page > 0 ? page - 1: page)
    let prd = getAllProducts();
    return prd.slice(start,start + pageSize)
}
